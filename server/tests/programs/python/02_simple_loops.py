# Simple Loops - Complexity: 2/6
# Tests basic loop constructs and iterations

# Simple for loop
for i in range(5):
    print(f"Count: {i}")

# While loop with condition
count = 0
while count < 3:
    print(f"While loop iteration: {count}")
    count += 1

# List iteration
fruits = ["apple", "banana", "orange"]
for fruit in fruits:
    print(f"Fruit: {fruit}")

print("Loop tests completed")
